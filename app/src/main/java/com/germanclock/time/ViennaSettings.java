package com.germanclock.time;

public class ViennaSettings extends Settings {
	
	private Integer rangeForViertel;
	private Integer rangeForHalb;
	private Integer rangeForDreiViertel;
	
	
	
	public Integer getRangeForViertel() {
		return rangeForViertel;
	}

	public void setRangeForViertel(Integer rangeForViertel) {
		this.rangeForViertel = rangeForViertel;
	}

	public Integer getRangeForHalb() {
		return rangeForHalb;
	}

	public void setRangeForHalb(Integer rangeForHalb) {
		this.rangeForHalb = rangeForHalb;
	}

	public Integer getRangeForDreiViertel() {
		return rangeForDreiViertel;
	}

	public void setRangeForDreiViertel(Integer rangeForDreiViertel) {
		this.rangeForDreiViertel = rangeForDreiViertel;
	}

	
	
	
	
	
}
